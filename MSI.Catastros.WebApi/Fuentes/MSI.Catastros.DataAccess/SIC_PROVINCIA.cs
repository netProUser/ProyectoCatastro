using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Common;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using BE = MSI.Catastros.BusinessEntities;
using Microsoft.Practices.EnterpriseLibrary.Data;

namespace MSI.Catastros.DataAccess
{
    public class SIC_PROVINCIA : Base
    {
        //――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――
        //Creado por	: Jaime Díaz Espinoza (18/04/2018)
        //Utilizado por	: BusinessLogic.SIC_PROVINCIA.Buscar
        //――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――――
        /// <summary>
        /// Permite listar los datos de la tabla SIC_PROVINCIA
        /// </summary>
        public IEnumerable<BE.SIC_PROVINCIA> Listar(BE.SIC_PROVINCIA entSIC_PROVINCIA)
        {
            List<BE.SIC_PROVINCIA> lista = new List<BE.SIC_PROVINCIA>();

            Database db = DatabaseFactory.CreateDatabase();
            DbCommand dbCommand = db.GetStoredProcCommand(EsquemaCatastro + ".PKG_MSISIC_MANT_PROVINCIA.PRC_LISTAR", new object[2]);
            db.SetParameterValue(dbCommand, "pvcCODDEPARTAMENTO", entSIC_PROVINCIA.CODDEPARTAMENTO);
            try
            {
                using (IDataReader reader = db.ExecuteReader(dbCommand))
                {
                    while (reader.Read())
                        lista.Add(new BE.SIC_PROVINCIA(reader, BE.SIC_PROVINCIA.Query.Listar));
                }
            }
            catch (Exception e)
            {
                throw new Exception(e.Message); ;
            }
            return lista;
        }
    }
}
