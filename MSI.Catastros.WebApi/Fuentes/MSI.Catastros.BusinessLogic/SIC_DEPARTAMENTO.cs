        /// <summary>
        /// Permite listar los datos de la tabla SIC_DEPARTAMENTO
        /// </summary>
        public ICollection<BE.SIC_DEPARTAMENTO> Listar()
        {
            List<BE.SIC_DEPARTAMENTO> lisQuery = null;
            try
            {
                IEnumerable<BE.SIC_DEPARTAMENTO> entQuery = new DA.SIC_DEPARTAMENTO().Listar();
                if (entQuery != null)
                {
                    lisQuery = entQuery.ToList();
                }
            }
            catch (Exception e)
            {
                throw e;
            }
            return lisQuery;
        }
