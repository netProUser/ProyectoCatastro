        /// <summary>
        /// Permite recuperar un registro de la tabla SIC_MANZAVIAVALOR
        /// </summary>
        public BE.SIC_MANZAVIAVALOR Recuperar(BE.SIC_MANZAVIAVALOR entSIC_MANZAVIAVALOR)
        {
            BE.SIC_MANZAVIAVALOR lisQuery = null;
            try
            {
                lisQuery = new DA.SIC_MANZAVIAVALOR().Recuperar(entSIC_MANZAVIAVALOR);
            }
            catch (Exception e)
            {
                throw e;
            }
            return lisQuery;
        }
